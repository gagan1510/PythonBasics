s = 'hello World!'

print(s.capitalize()) # capitalize function capitalizes the first letter of the string

print(s.count('o')) # no. of os in a string

print(s.find('o')) # prints the position at which the o is found

print(s.center(20, 'z')) # this adds extra zs and makes the total length of the string as 20

print(s.partition(" ")) # this splits the string into two parts, one before and after the ' '

